//---------------------------------------------------------------------------------------
//戦闘計算
//---------------------------------------------------------------------------------------
int CBattle::DamageCalculation(int Attack,int Defence)
{
	int Damage;				//ダメージ計算値格納

	//ダメージの計算
	Damage =  Attack - Defence;

	//ダメージの値がマイナスになったら、0にする
	if(Damage < 0)
		Damage = 1;


	//ダメージ値を返却
	return Damage;
}