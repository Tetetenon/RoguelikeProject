//メッシュ　オブジェクト実装
#include "MeshObj.h"

//親クラスはC3DObj

//---------------------------------------------------------------------------------------
//コンストラクタ
//---------------------------------------------------------------------------------------
CMeshObj::CMeshObj(CGameScene* pScene):
C3DObj(pScene)
{
	m_uID = ID_MESHOOBJ;
	//メッシュの中身を初期化
	m_pMesh = NULL;
}

//---------------------------------------------------------------------------------------
//デストラクタ
//---------------------------------------------------------------------------------------
CMeshObj::~CMeshObj(void)
{
}


//---------------------------------------------------------------------------------------
//初期化
//---------------------------------------------------------------------------------------
void CMeshObj::Init()
{

}
//---------------------------------------------------------------------------------------
//終了処理
//---------------------------------------------------------------------------------------
void CMeshObj::Fin()
{
	C3DObj::Fin();
}
//---------------------------------------------------------------------------------------
//更新処理
//---------------------------------------------------------------------------------------
void CMeshObj::Update()
{
	C3DObj::Update();
}
//---------------------------------------------------------------------------------------
//描画処理
//---------------------------------------------------------------------------------------
void CMeshObj::Draw()
{
	//メッシュを使用していた場合、メッシュ側の描画処理を行う
	if(m_pMesh)
	{
			m_pMesh -> DrawNoAlpha(m_world);
	}
}

//---------------------------------------------------------------------------------------
//描画処理
//---------------------------------------------------------------------------------------
void CMeshObj::DrawAlpha()
{
	//メッシュを使用していた場合、メッシュ側の描画処理を行う
	if(m_pMesh)
	{
			m_pMesh -> DrawAlpha(m_world);
	}

}

DWORD CMeshObj::GetPieceCount()
{
	if (m_pMesh) {
		return m_pMesh->GetPieceCount();
	}
	return 0;
}

void CMeshObj::GetInitPiece(PIECE* pPiece)
{
	if (m_pMesh) {
		m_pMesh->GetInitPiece(pPiece);
	}
}