#include "EffectObj.h"
#include "ModelManager.h"
#include "GameScene.h"


//---------------------------------------------------------------------------------------
//コンストラクタ
//---------------------------------------------------------------------------------------
CEffectObj::CEffectObj(CGameScene *pScene):
CMeshObj(pScene),
m_nLifeTime(OBJ_LIFE)
{
}

//---------------------------------------------------------------------------------------
//デストラクタ
//---------------------------------------------------------------------------------------
CEffectObj::~CEffectObj(void)
{
}

//---------------------------------------------------------------------------------------
//更新
//---------------------------------------------------------------------------------------
void CEffectObj::Update()
{
	//生存時間を減算
	m_nLifeTime --;

	//もし、生存時間が無くなれば、
	if(m_nLifeTime <= 0)
	{
		//自身の削除
		delete this;

		return;
	}

	//親関数の更新
	CMeshObj::Update();
}
//---------------------------------------------------------------------------------------
//終了処理
//---------------------------------------------------------------------------------------
void CEffectObj::Fin()
{
	//親の更新
	CMeshObj::Fin();

	//自身の削除
	delete this;
}